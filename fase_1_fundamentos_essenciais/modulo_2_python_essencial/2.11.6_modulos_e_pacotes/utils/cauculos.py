def soma(a, b):
    """
    Soma dois números, seja eles inteiros ou reais.

    Args:
        a (int or float): Primeiro número da equação.
        b (int or float): Segundo número da equação.


    Returns:
        int or float: Retorna a soma dos dois números.
    """
    return a + b


def subtracao(a, b):
    """
    Subtrai dois números, seja eles inteiros ou reais.

    Args:
        a (int or float): Primeiro número da equação.
        b (int or float): Segundo número da equação.


    Returns:
        int or float: Retorna a subtração dos dois números.
    """
    return a - b
